const negativePrompt = "低品質、崩れた人体、余分な指、不自然な手足、顔の歪み、左右非対称すぎる目、読みにくい構図、過剰な文字、ロゴ、透かし、背景の破綻、服装の大きな変更、参考画像と違う人物";

const items = JSON.parse(fs.readFileSync(batchPath, "utf8").replace(/^\uFEFF/, ""));
for (const item of items) {
  if (item.primary === "summary_sheet") {
    item.usageStatus = "reference_notes";
    item.promptLabel = "設計メモ";
    continue;
  }
  const update = articleReadyPrompts[item.id];
  if (!update) continue;
  item.titleJa = update.titleJa;
  item.promptJa = update.promptJa;
  item.negativeJa = negativePrompt;
  item.usageStatus = "article_ready";
  item.promptLabel = "記事用プロンプト";
  item.status = "ready_for_article_prompt";
}

fs.writeFileSync(batchPath, JSON.stringify(items, null, 2) + "\n", "utf8");

const counts = items.reduce((acc, item) => {
  const key = item.usageStatus || "unset";
  acc[key] = (acc[key] || 0) + 1;
  return acc;
}, {});
